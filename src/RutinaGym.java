    private void jCBrutinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBrutinaActionPerformed
        Object item = jCBrutina.getSelectedItem();
        String caso = (String) item;
        listModel.clear();
        
        System.out.println(caso);
        
        
        switch (caso){
            case "Pecho-Hombro-Triceps":{
                String[] cadena = {"Press plano", "Press inclinado", "Peck Deck", "Averturas en Peck Deck", "Elevaciones laterales", "Extension de triceps", "Extension tras nuca"};
                for (String i: cadena){
                    listModel.addElement(i);
                }   
                break;
            }
            case "Espalda-Biceps":{
                String[] cadena = {"Jalon al pecho", "Remo", "Pull over", "Curl predicador", "Curl con codo detras del hombro"};
                for (String i: cadena){
                    listModel.addElement(i);
                }   
                break;
            }
            case "Pierna":{
                String[] cadena = {"Aduptor", "Sentadilla", "Extension de cuadriceps", "Prensa polea", "Isquios", "Aductor", "Soleo"};
                for (String i: cadena){
                    listModel.addElement(i);
                }   
                break;
            }
        }
    }//GEN-LAST:event_jCBrutinaActionPerformed

